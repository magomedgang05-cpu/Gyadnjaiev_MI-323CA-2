# 1. Функция поиска
def task_1(arr):
    for i in arr:
        if i == 100:
            return True
    return False

# Сложность: O(n) – линейная
# Пояснение: В худшем случае нужно пройти по всем элементам массива, 
# но в среднем может найти раньше. Однако Big O оценивает худший сценарий.

# 2. Функция доступа
def task_2(arr):
    return arr[0] + arr[-1]

# Сложность: O(1) – константная
# Пояснение: Доступ к элементам по индексу в списке Python выполняется за O(1).
# Функция делает фиксированное количество операций независимо от размера массива.

# 3. Функция пар
def task_3(arr):
    count = 0
    for i in arr:
        for j in arr:
            if i == j:
                count += 1
    return count

# Сложность: O(n²) – квадратичная
# Пояснение: Двойной вложенный цикл. Для каждого из n элементов
# выполняем n операций, итого n * n = n² операций.